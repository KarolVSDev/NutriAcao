    <p style="margin-top:5%; margin-left:2%;"><b>Horta de Compostagem</b><br>

    Materiais Necessários:<br>




Caixote de madeira ou recipiente para compostagem

<br>Restos de comida (vegetais, cascas de frutas, borras de café, cascas de ovos trituradas, etc.)

<br>Material seco (folhas secas, aparas de grama, serragem, papelão picado)

<br>Água<br><br>

Instruções:<br>

Montagem do Composto: Comece colocando uma camada de material seco no fundo do seu caixote de compostagem. Isso ajudará a melhorar a drenagem e aeração.




<br>Adição dos Restos de Comida: Em seguida, adicione uma camada de restos de comida. Isso inclui vegetais, frutas, borras de café, cascas de ovos (trituradas) e outros resíduos orgânicos da sua cozinha.




<br>Adição de Material Seco: Cubra os restos de comida com outra camada de material seco. Isso ajuda a evitar odores e a manter a relação correta de carbono e nitrogênio para uma boa decomposição.




<br>Umidade: Regue o composto conforme necessário para manter a umidade. Ele deve estar úmido, mas não encharcado.




<br>Aeração: De vez em quando, revolva o composto para promover a aeração e acelerar o processo de decomposição.




<br>Tempo de Decomposição: Dependendo das condições, o composto estará pronto para ser usado em sua horta dentro de algumas semanas a alguns meses. Quando estiver escuro, cheirar a terra e não reconhecer mais os materiais originais, está pronto para uso.

<br><br>

Observações Importantes:




<br>Evite adicionar restos de carne, produtos lácteos, óleos ou alimentos processados ao composto, pois eles podem atrair pragas e retardar o processo de decomposição.




<br>Mantenha uma proporção equilibrada de material úmido (restos de comida) e material seco (folhas, papelão) para garantir uma decomposição eficiente.




<br>Se você não tiver espaço para uma compostagem tradicional, considere usar uma composteira doméstica, que é menor e pode ser usada em ambientes internos.

