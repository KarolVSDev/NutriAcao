    <p style="margin-top:5%; margin-left:2%;"><b>Bolo de Casca de Banana</b><br>

Ingredientes:<br>




4 cascas de banana

3 ovos

2 xícaras de açúcar

2 colheres (sopa) de manteiga

2 xícaras de farinha de rosca

1 colher (sopa) de fermento em pó<br><br>

Modo de Preparo:<br>




Bata as cascas de banana com um pouco de água no liquidificador.

Acrescente os ovos, o açúcar e a manteiga, batendo bem.

Misture a farinha de rosca e o fermento.

Asse em forno pré-aquecido a 180°C por cerca de 40 minutos.</p>

